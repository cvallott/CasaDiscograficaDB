--Creazione tabelle

CREATE TABLE Artista (
    nomeArte VARCHAR(20) PRIMARY KEY,
    annoInizioAttivita INT NOT NULL
);

CREATE TABLE Cliente (
    CF VARCHAR(16) PRIMARY KEY,
    nome VARCHAR(20) NOT NULL,
    cognome VARCHAR(20) NOT NULL,
    annoNascita DATE NOT NULL,
    telefono VARCHAR(9) NOT NULL,
    mail VARCHAR(50) NOT NULL,
    artista VARCHAR(20) NOT NULL,
    FOREIGN KEY (artista) REFERENCES Artista(nomeArte) ON DELETE CASCADE
);

CREATE TABLE Contratto (
    id VARCHAR(5) PRIMARY KEY,
    durata VARCHAR(20) NOT NULL CHECK (durata IN ('determinato', 'indeterminato')),
    remunerazione INT NOT NULL,
    dataFirma DATE NOT NULL,
    cliente VARCHAR(16) NOT NULL,
    FOREIGN KEY (cliente) REFERENCES Cliente(CF) ON DELETE CASCADE
);

CREATE TABLE Concerto (
    data DATE NOT NULL,
    artista VARCHAR(20) NOT NULL,
    soldOut BOOLEAN NOT NULL,
    indirizzo VARCHAR(50) NOT NULL,
    numeroPosti INT NOT NULL,
    luogo VARCHAR(20) NOT NULL CHECK (luogo IN ('locale', 'palazzetto', 'stadio')),
    PRIMARY KEY (data, artista),
    FOREIGN KEY (artista) REFERENCES Artista(nomeArte) ON DELETE CASCADE
);

CREATE TABLE Cantante (
    artista VARCHAR(20) PRIMARY KEY,
    tipoVoce VARCHAR(20) NOT NULL CHECK (tipoVoce IN ('Soprano', 'Contralto', 'Tenore', 'Basso')),
    FOREIGN KEY (artista) REFERENCES Artista(nomeArte) ON DELETE CASCADE
);

CREATE TABLE Band (
    artista VARCHAR(20) PRIMARY KEY,
    numComponenti INT NOT NULL,
    frontman VARCHAR(20),
    FOREIGN KEY (artista) REFERENCES Artista(nomeArte) ON DELETE CASCADE,
    FOREIGN KEY (frontman) REFERENCES Cantante(artista) ON DELETE CASCADE
);

CREATE TABLE Producer (
    artista VARCHAR(20) PRIMARY KEY,
    competenzaStrumentale VARCHAR(20) NOT NULL CHECK (competenzaStrumentale IN ('principiante', 'intermedio', 'esperto')),
    band VARCHAR(20),
    FOREIGN KEY (artista) REFERENCES Artista(nomeArte) ON DELETE CASCADE,
    FOREIGN KEY (band) REFERENCES Band(artista) ON DELETE CASCADE
);

CREATE TABLE Strumentista (
    artista VARCHAR(20) PRIMARY KEY,
    competenzaStrumentale VARCHAR(20) NOT NULL CHECK (competenzaStrumentale IN ('principiante', 'intermedio', 'esperto')),
    band VARCHAR(20),
    FOREIGN KEY (artista) REFERENCES Artista(nomeArte) ON DELETE CASCADE,
    FOREIGN KEY (band) REFERENCES Band(artista) ON DELETE CASCADE
);

CREATE TABLE Strumento (
    nome VARCHAR(20) PRIMARY KEY
);

CREATE TABLE Utilizzo (
    strumentista VARCHAR(20) NOT NULL,
    strumento VARCHAR(20) NOT NULL,
    PRIMARY KEY (strumentista, strumento),
    FOREIGN KEY (strumentista) REFERENCES Strumentista(artista) ON DELETE CASCADE,
    FOREIGN KEY (strumento) REFERENCES Strumento(nome) ON DELETE CASCADE
);

CREATE TABLE Genere (
    tipo VARCHAR(20) PRIMARY KEY
);

CREATE TABLE Stile (
    artista VARCHAR(20) NOT NULL,
    genere VARCHAR(20) NOT NULL,
    PRIMARY KEY (artista, genere),
    FOREIGN KEY (artista) REFERENCES Artista(nomeArte) ON DELETE CASCADE,
    FOREIGN KEY (genere) REFERENCES Genere(tipo) ON DELETE CASCADE
);

CREATE TABLE StudioRegistrazione (
    città VARCHAR(20) NOT NULL,
    indirizzo VARCHAR(50) NOT NULL,
    PRIMARY KEY (città, indirizzo)
);

CREATE TABLE Album (
    id INT PRIMARY KEY,
    nome VARCHAR(20) NOT NULL,
    numCanzoni INT NOT NULL,
    durata INT NOT NULL,
    numAscolti INT NOT NULL,
    dataUscita DATE NOT NULL,
    artista VARCHAR(20) NOT NULL,
    genere VARCHAR(20) NOT NULL,
    cittàReg VARCHAR(20) NOT NULL,
    indReg VARCHAR(50) NOT NULL,
    FOREIGN KEY (artista) REFERENCES Artista(nomeArte) ON DELETE CASCADE,
    FOREIGN KEY (genere) REFERENCES Genere(tipo) ON DELETE CASCADE,
    FOREIGN KEY (cittàReg, indReg) REFERENCES StudioRegistrazione(città, indirizzo) ON DELETE CASCADE
);

CREATE TABLE Canzone (
    id INT PRIMARY KEY,
    titolo VARCHAR(20) NOT NULL,
    durata INT NOT NULL,
    numAscolti INT NOT NULL,
    dataUscita DATE NOT NULL,
    artista VARCHAR(20) NOT NULL,
    genere VARCHAR(20) NOT NULL,
    album INT,
    cittàReg VARCHAR(20) NOT NULL,
    indReg VARCHAR(50) NOT NULL,
    FOREIGN KEY (artista) REFERENCES Artista(nomeArte) ON DELETE CASCADE,
    FOREIGN KEY (genere) REFERENCES Genere(tipo) ON DELETE CASCADE,
    FOREIGN KEY (album) REFERENCES Album(id) ON DELETE CASCADE,
    FOREIGN KEY (cittàReg, indReg) REFERENCES StudioRegistrazione(città, indirizzo) ON DELETE CASCADE
);

CREATE TABLE Certificazione (
    id INT PRIMARY KEY,
    data DATE NOT NULL,
    tipologia VARCHAR(10) CHECK (tipologia IN ('oro', 'platino', 'diamante'))
);

CREATE TABLE PremioCanzone (
    canzone INT NOT NULL,
    certificazione INT NOT NULL,
    PRIMARY KEY (canzone, certificazione),
    FOREIGN KEY (canzone) REFERENCES Canzone(id) ON DELETE CASCADE,
    FOREIGN KEY (certificazione) REFERENCES Certificazione(id) ON DELETE CASCADE
);

CREATE TABLE PremioAlbum (
    album INT NOT NULL,
    certificazione INT NOT NULL,
    PRIMARY KEY (album, certificazione),
    FOREIGN KEY (album) REFERENCES Album(id) ON DELETE CASCADE,
    FOREIGN KEY (certificazione) REFERENCES Certificazione(id) ON DELETE CASCADE
);

